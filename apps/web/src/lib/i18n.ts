export type Translations = typeof translations.en;

// ─── LANGUAGE RESOLUTION ──────────────────────────────────────────────────────
export function detectLanguage(): LangCode {
  // 1. LocalStorage user preference
  try {
    const stored = localStorage.getItem(LS_KEY) as LangCode | null;
    if (stored && stored in translations) return stored;
  } catch (_) {}

  // 2. Browser language
  const browser = (navigator.language || '').toLowerCase();
  const codes = Object.keys(translations) as LangCode[];
  for (const code of codes) {
    if (browser.startsWith(code)) return code;
  }

  // 3. Default: English
  return 'en';
}

export function setLanguage(code: LangCode): void {
  try { localStorage.setItem(LS_KEY, code); } catch (_) {}
}

export function getTranslations(code: LangCode): Translations {
  return (translations[code] ?? translations.en) as unknown as Translations;
}

export function getLangMeta(code: LangCode) {
  return SUPPORTED_LANGUAGES.find(l => l.code === code) ?? SUPPORTED_LANGUAGES[0];
}
